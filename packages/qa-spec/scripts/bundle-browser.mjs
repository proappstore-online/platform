/**
 * Bundle the DOM runner into a self-contained IIFE string, exported as a TS
 * module constant. The host worker serves it to the runner page; the
 * qa-worker injects it via page.evaluate. Exposes `globalThis.__pasQaRunner`.
 */
import { build } from 'esbuild';
import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = dirname(fileURLToPath(import.meta.url));
const entry = resolve(root, '../src/dom-runner.ts');
const outDir = resolve(root, '../dist');

const result = await build({
  entryPoints: [entry],
  bundle: true,
  write: false,
  format: 'iife',
  globalName: '__pasQaRunner',
  platform: 'browser',
  target: 'es2020',
  minify: true,
});

const code = result.outputFiles[0].text;
await mkdir(outDir, { recursive: true });
await writeFile(
  resolve(outDir, 'browser-bundle.js'),
  `// Auto-generated by scripts/bundle-browser.mjs — do not edit.\nexport const DOM_RUNNER_BUNDLE = ${JSON.stringify(code)};\n`,
);
await writeFile(
  resolve(outDir, 'browser-bundle.d.ts'),
  `/** Minified IIFE of dom-runner.ts; exposes globalThis.__pasQaRunner. */\nexport declare const DOM_RUNNER_BUNDLE: string;\n`,
);
console.log(`browser bundle: ${(code.length / 1024).toFixed(1)} KB`);
