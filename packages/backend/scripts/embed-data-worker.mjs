// Codegen: embed the data-worker bundle into the backend at build time.
//
// The backend deploys data workers by uploading a script to the Cloudflare
// Workers API. That script used to be fetched at runtime from a raw-GitHub URL
// pointing at a committed `dist/worker.js` — but that file is now gitignored
// (commit 465b562), so the URL 404s and every deploy failed (issue #66).
//
// Instead we build the data-worker bundle and emit it as a string constant the
// backend imports directly: versioned + tested with the backend, no runtime
// GitHub dependency. Run from `prebuild`/`pretypecheck`/`pretest` and from the
// wrangler `[build]` hook, so the generated file always exists before tsc,
// vitest, and `wrangler deploy`/`dev`.
//
// Idempotent: rebuilds the data-worker bundle and overwrites the generated file.

import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const backendRoot = resolve(scriptDir, '..');
const repoRoot = resolve(backendRoot, '../..');
const bundlePath = resolve(repoRoot, 'packages/data-worker/dist/worker.js');
const outDir = resolve(backendRoot, 'src/generated');
const outPath = resolve(outDir, 'data-worker-bundle.ts');

// Build the data-worker bundle via its own canonical esbuild invocation (DRY —
// reuses the flags in packages/data-worker/package.json).
execSync('pnpm --filter @proappstore/data-worker build', {
  cwd: repoRoot,
  stdio: 'inherit',
});

const script = readFileSync(bundlePath, 'utf8');
if (!script.trim()) {
  throw new Error(`embed-data-worker: built bundle at ${bundlePath} is empty`);
}

mkdirSync(outDir, { recursive: true });
writeFileSync(
  outPath,
  `// AUTO-GENERATED by scripts/embed-data-worker.mjs — do not edit, do not commit.\n` +
    `// The data-worker bundle, embedded so the backend can upload it without a\n` +
    `// runtime GitHub fetch. Regenerated on every build/typecheck/test/deploy.\n` +
    `export const DATA_WORKER_BUNDLE = ${JSON.stringify(script)};\n`,
);

console.log(`embed-data-worker: wrote ${outPath} (${script.length} bytes)`);
